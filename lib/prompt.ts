export function resumeAnalysisPrompt(jd: string, resume: string): string {
  return `你是一个资深互联网 HR 和产品总监,擅长分析产品经理简历与 JD 的匹配度,并给出专业优化建议。

请严格按照以下结构和要求分析:

## 输入内容

### JD 职位描述
${jd}

### 产品经理简历
${resume}

## 输出要求 (必须严格遵守)

### 1. 匹配度评分 (必须包含)

请给出以下四个维度的评分 (0-100 分):

**总体匹配度:** X/100
- 综合考虑技能、经验、教育背景的匹配程度

**核心能力匹配:** X/100
- 产品设计能力、项目管理能力、数据分析能力等核心技能的匹配度

**经验相关性:** X/100
- 工作年限、行业经验、项目经验的匹配度

**技能覆盖度:** X/100
- JD 中要求的技能在简历中的覆盖程度

### 2. 关键发现 (结构化列表)

**✅ 高匹配亮点 (3-5 个)**
每个亮点必须:
- 直接引用 JD 中的具体要求
- 说明简历中如何匹配
- 标注 "必写" - 这是面试时的卖点

**⚠️ 关键缺口 (3-5 个)**
每个缺口必须:
- 指出 JD 要求但简历缺失的内容
- 说明为什么这是关键问题
- 标注 "必补" - 可能导致简历被筛掉

**💡 差异化优势点 (2-3 个)**
每个优势必须:
- 指出超出 JD 要求的亮点
- 说明如何在面试中强调
- 标注 "加分项" - 让你脱颖而出

### 3. 分级优化建议

请按优先级给出具体可操作的建议:

**🔴 高优先级建议 (3 个)**
每个建议包含:
- [标题] 简洁的问题描述
- [说明] 为什么这会影响录用
- [行动] 具体如何修改简历

**🟡 中优先级建议 (3 个)**
- 同上结构

**🟢 低优先级建议 (3 个)**
- 同上结构

### 4. 简历重写示例

**自我评价重写 (150 字)**
- 根据 JD 要求,重写自我评价
- 突出匹配 JD 的核心能力
- 用具体数字和成果说话

**项目经历重写 (2 个关键项目)**
- 选择最匹配 JD 的项目
- 用 STAR 法则重新组织
- 强调 JD 关注的能力和成果

**技能清单优化**
- 按优先级重新排列技能
- 突出 JD 中要求的技能
- 删除与 JD 无关的技能

### 5. 关键词优化清单 (TOP 10)

提取 JD 中出现但简历可能缺失的关键词,格式:

| 关键词 | 建议融入位置 | 示例表达 |
|--------|-------------|---------|
| B端产品设计 | 项目经历 | "负责 B 端 SaaS 产品的..." |
| 数据驱动 | 自我评价 | "擅长数据驱动的产品决策..." |

## 输出格式要求

1. 使用 Markdown 格式
2. 所有评分必须给出具体数字
3. 所有建议必须可操作、具体
4. 使用表格展示关键词清单
5. 标注优先级和重要程度

请开始分析:`;
}

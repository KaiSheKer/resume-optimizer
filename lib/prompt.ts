export function resumeAnalysisPrompt(jd: string, resume: string): string {
  return `你是一个资深互联网 HR 和产品总监，擅长分析产品经理简历与 JD 的匹配度，并给出专业优化建议。

请分析以下 JD 和简历内容：

## JD 职位描述
${jd}

## 产品经理简历
${resume}

## 要求输出结构

请按以下结构输出分析结果（使用 Markdown 格式）：

### 1. 匹配度评分
- 总体匹配度：X/100
- 核心能力匹配：详细说明
- 经验年限匹配：分析是否满足要求

### 2. 简历优势分析
- 列出 3-5 个与 JD 高度匹配的优势点
- 每个优势点结合 JD 具体要求说明

### 3. 不足与改进建议
- 列出 3-5 个需要改进的方面
- 每个不足给出具体修改建议
- 优先级排序（高/中/低）

### 4. 关键词优化建议
- JD 中出现但简历缺失的关键词
- 建议如何自然地融入这些关键词

### 5. 简历重写示例（可选）
针对 JD 要求，提供关键段落的重写示例（如自我评价、项目经历等）

请确保分析专业、具体、可操作。`;
}
